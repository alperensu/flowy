export const getArtistData = (artistName) => {
    if (!artistName) return null;

    // Check if we have predefined data (case-insensitive)
    const normalizedName = artistName.toLowerCase();
    const popularKey = Object.keys(popularArtists).find(key => key.toLowerCase() === normalizedName);

    if (popularKey) {
        return popularArtists[popularKey];
    }

    // Generate deterministic data based on name
    const hash = artistName.split('').reduce((acc, char) => acc + char.charCodeAt(0), 0);
    const imageIndex = hash % genericImages.length;

    return {
        name: artistName,
        image: genericImages[imageIndex],
        listeners: (hash * 12345).toLocaleString('tr-TR'),
        verified: hash % 2 === 0,
        tracks: [
            { id: `gen_${hash}_1`, title: `${artistName} Hit`, artist: artistName, album: 'Greatest Hits', duration: 180 + (hash % 60), plays: (hash * 9876).toLocaleString('tr-TR'), image: genericImages[imageIndex] },
            { id: `gen_${hash}_2`, title: 'Love Song', artist: artistName, album: 'Love & Life', duration: 200 + (hash % 40), plays: (hash * 5432).toLocaleString('tr-TR'), image: genericImages[imageIndex] },
            { id: `gen_${hash}_3`, title: 'Night Vibes', artist: artistName, album: 'Night', duration: 170 + (hash % 50), plays: (hash * 3210).toLocaleString('tr-TR'), image: genericImages[imageIndex] },
            { id: `gen_${hash}_4`, title: 'Summer Time', artist: artistName, album: 'Summer', duration: 190 + (hash % 30), plays: (hash * 1111).toLocaleString('tr-TR'), image: genericImages[imageIndex] },
            { id: `gen_${hash}_5`, title: 'Intro', artist: artistName, album: 'The Beginning', duration: 120 + (hash % 20), plays: (hash * 500).toLocaleString('tr-TR'), image: genericImages[imageIndex] },
        ]
    };
};
